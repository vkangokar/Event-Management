    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageCustomerTbl.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
        LabTestWorkRequest request = (LabTestWorkRequest)manageCustomerTbl.getValueAt(selectedRow, 0);
        
     
        if(request.getStatus().equalsIgnoreCase("completed")){
            JOptionPane.showMessageDialog(null, "already booked");
            return;
        }
     
        request.setStatus("Completed");
        
        ManageCustomerProcessRequest manageCustomerProcessRequest = new ManageCustomerProcessRequest(userProcessContainer, account, organization,enterprise,business,request);
        userProcessContainer.add("ManageCustomerProcessRequest", manageCustomerProcessRequest);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_processBtnActionPerformed

    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
          int selectedRow = manageCustomerTbl.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
        WorkRequest request = (WorkRequest)manageCustomerTbl.getValueAt(selectedRow, 0);
        request.setReceiver(account);
        request.setStatus("Processing");
        populateTable();
    }//GEN-LAST:event_assignBtnActionPerformed

