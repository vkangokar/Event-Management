        public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)manageCustomerTbl.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[12];
            row[0] = request;
            row[1] = request.getHallType();
            row[2] = request.getFoodType();
            row[3] = request.getFoodPrice();
            row[4] = request.getHallPrice();
            row[5] = request.getStatus();
            row[6] = request.getQuantity();
            row[7] = request.getDate();
            row[8] = request.getSender().getEmployee().getName();
            //row[9] = request.getReceiver().getEmployee().getName();
            row[9] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            
           // row[6] = request.getSender();
            
            
            model.addRow(row);
        }
    }
